/**
 * Holds the values for severity of a validation result.
 */
export enum ValidationResultSeverity {

    /**
     * The validation result is unknown.
     */
    Unknown = 0,

    /**
     * The validation result is a warning.
     */
    Information = 1,

    /**
     * The validation result is a warning.
     */
    Warning = 2,

    /**
     * The validation result is an error.
     */
    Error = 3
}
